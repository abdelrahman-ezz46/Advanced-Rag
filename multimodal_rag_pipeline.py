#  CONTEXTUAL RETRIEVER  (Anthropic-style contextual retrieval, local edition)
# ══════════════════════════════════════════════════════════════════════════════

class ContextualRetriever:
    """
    Enriches each chunk with a *situating context* generated by a local LLM
    that has access to the **entire source document**.

    ── Why this matters ────────────────────────────────────────────────────────
    When a chunk is embedded and stored in a vector DB, it loses the document-
    level context that makes it interpretable.  A chunk like:

        "Revenue declined by 12 % quarter-over-quarter."

    is ambiguous: which company? which quarter? which product line?

    The contextual retrieval technique fixes this by prepending a short,
    LLM-generated situating sentence BEFORE the chunk text:

        "This chunk is from the Q3 2024 earnings report of Acme Corp,
         specifically the section on North American retail segment performance.
         Revenue declined by 12 % quarter-over-quarter."

    The enriched text (context + chunk) is what gets embedded and what BM25
    indexes.  The original chunk_text is preserved separately for display.

    ── Parallelism ─────────────────────────────────────────────────────────────
    Each chunk requires one LLM call.  These are independent, so they run
    concurrently via `ThreadPoolExecutor`.  For a 20-chunk document with a
    ~1 s/call local model, wall time drops from ~20 s → ~3–5 s (depending on
    Ollama's concurrency setting and GPU VRAM).

    ── Output fields added to each ChunkDict ───────────────────────────────────
      context             : str  — the raw 1–2 sentence situating context
      contextualized_text : str  — context prepended to chunk_text
                                   (this is what you embed and BM25-index)

    Install : pip install ollama  (already required)
    Setup   : ollama pull llama3  (already required)
    """

    MODEL = "llama3"

    # Maximum number of parallel Ollama calls.
    # Keep ≤ 4 for CPU-only; raise to 8–16 with a GPU that has enough VRAM.
    DEFAULT_MAX_WORKERS = 4

    # ── Prompt templates ─────────────────────────────────────────────────────
    # These follow Anthropic's published prompt structure for contextual
    # retrieval almost verbatim, adapted for local Llama 3.

    SYSTEM_PROMPT = (
        "You are a precision document analyst assisting with the preparation "
        "of a Retrieval-Augmented Generation (RAG) knowledge base. "
        "You will be given a full document and a specific chunk of that document. "
        "Your ONLY job is to write a SHORT situating context (1–2 sentences, "
        "≤ 60 words) that places the chunk within the broader document so a "
        "reader encountering the chunk in complete isolation would understand "
        "what document it came from, what section it belongs to, and what "
        "higher-level concept it relates to.\n\n"
        "OUTPUT RULES:\n"
        "- Output ONLY the context sentence(s). No preamble, no labels, "
        "  no 'Here is the context:', no quotation marks around your answer.\n"
        "- Never summarise the chunk itself — only situate it.\n"
        "- Write in the third person, present tense.\n"
        "- Be specific: name the document, section, or topic explicitly."
    )

    USER_TEMPLATE = (
        "<document>\n"
        "{full_document}\n"
        "</document>\n\n"
        "Here is the specific chunk that needs to be situated:\n\n"
        "<chunk>\n"
        "{chunk_text}\n"
        "</chunk>\n\n"
        "Write the situating context now:"
    )

    def __init__(
        self,
        model: str = MODEL,
        max_workers: int = DEFAULT_MAX_WORKERS,
    ) -> None:
        self._model       = model
        self._max_workers = max_workers

    # ── Public API ────────────────────────────────────────────────────────────

    def enrich(
        self,
        chunks: list[ChunkDict],
        full_document: str,
    ) -> list[ChunkDict]:
        """
        Enrich every chunk in *chunks* with a situating context.

        Parameters
        ----------
        chunks : list[ChunkDict]
            Output of ChunkingAgent.chunk() — list of dicts with at least
            a 'chunk_text' key.
        full_document : str
            The complete unified Markdown document (metadata header + content).
            Passed to the LLM on every call so it can situate each chunk.

        Returns
        -------
        list[ChunkDict]
            Same list, each dict extended with:
              'context'             — the generated situating sentence(s)
              'contextualized_text' — context + "\\n\\n" + chunk_text
              'context_error'       — present only if contextualisation failed;
                                      the chunk is still returned unmodified.
        """
        if not chunks:
            return chunks

        logger.info(
            "[ContextualRetriever] Enriching %d chunks with situating context "
            "(model=%s, workers=%d)…",
            len(chunks),
            self._model,
            self._max_workers,
        )

        enriched: list[ChunkDict] = [{}] * len(chunks)  # pre-allocate slots

        with ThreadPoolExecutor(max_workers=self._max_workers) as pool:
            # Submit one future per chunk, carrying the original index so we
            # can reassemble in order after parallel completion.
            future_to_idx = {
                pool.submit(
                    self._contextualise_one,
                    chunk,
                    full_document,
                    idx,
                ): idx
                for idx, chunk in enumerate(chunks)
            }

            for future in as_completed(future_to_idx):
                idx = future_to_idx[future]
                try:
                    enriched[idx] = future.result()
                except Exception as exc:
                    # A single chunk failure must NOT abort the whole batch.
                    logger.warning(
                        "[ContextualRetriever] Chunk %d failed: %s — "
                        "returning original chunk without context.",
                        idx,
                        exc,
                    )
                    original = dict(chunks[idx])
                    original["context"]             = ""
                    original["contextualized_text"] = str(original.get("chunk_text", ""))
                    original["context_error"]        = str(exc)
                    enriched[idx]                    = original

        succeeded = sum(1 for c in enriched if "context_error" not in c)
        logger.info(
            "[ContextualRetriever] Done — %d/%d chunks successfully contextualised.",
            succeeded,
            len(chunks),
        )
        return enriched

    # ── Internal helpers ──────────────────────────────────────────────────────

    def _contextualise_one(
        self,
        chunk: ChunkDict,
        full_document: str,
        idx: int,
    ) -> ChunkDict:
        """
        Generate a situating context for a single chunk and return an
        enriched copy of the chunk dict.

        This method is called from a thread pool, so it must be thread-safe.
        The `ollama` library uses its own HTTP session per call, which is safe.
        """
        try:
            import ollama  # type: ignore
        except ImportError as exc:
            raise BaseProcessor.ProcessingError(
                "ollama is not installed. Run: pip install ollama"
            ) from exc

        chunk_text = str(chunk.get("chunk_text", ""))

        user_message = self.USER_TEMPLATE.format(
            full_document=full_document,
            chunk_text=chunk_text,
        )

        logger.debug("[ContextualRetriever] Contextualising chunk %d…", idx)

        try:
            response = ollama.chat(
                model=self._model,
                messages=[
                    {"role": "system", "content": self.SYSTEM_PROMPT},
                    {"role": "user",   "content": user_message},
                ],
                options={
                    # Slight creativity (0.2) produces more natural phrasing
                    # than fully deterministic (0.0) for short prose sentences.
                    "temperature": 0.2,
                    # Context window: full_document + chunk.
                    # Increase if documents exceed ~6 000 tokens.
                    "num_ctx": 8192,
                },
            )
        except Exception as exc:
            raise BaseProcessor.ProcessingError(
                f"Ollama call failed for chunk {idx}: {exc}"
            ) from exc

        context: str = response["message"]["content"].strip()

        if not context:
            raise BaseProcessor.ProcessingError(
                f"LLM returned empty context for chunk {idx}."
            )

        # Build the enriched chunk: copy all existing keys and add new ones.
        enriched = dict(chunk)
        enriched["context"] = context
        # This is what gets embedded — context first, then the chunk itself.
        enriched["contextualized_text"] = f"{context}\n\n{chunk_text}"

        logger.debug(
            "[ContextualRetriever] Chunk %d contextualised (%d chars context).",
            idx,
            len(context),
        )
        return enriched




class MultimodalRAGPipeline:
    """
    High-level orchestrator that wires:

        DataRouter → MarkdownUnifier → ChunkingAgent → ContextualRetriever

    into a single `ingest()` call.

    The `contextual_retrieval` flag (default: True) controls whether the
    ContextualRetriever step runs.  Disable it to reproduce v1.0 behaviour or
    to benchmark the improvement contextual retrieval provides.

    Usage
    -----
    >>> pipeline = MultimodalRAGPipeline()
    >>> chunks = pipeline.ingest("quarterly_report.pdf")
    >>> for c in chunks:
    ...     print(c["context"])              # situating sentence  ← NEW
    ...     print(c["contextualized_text"])  # what gets embedded  ← NEW
    ...     print(c["summary"])
    ...     print(c["keywords"])
    """

    def __init__(
        self,
        whisper_model       : str  = AudioProcessor.DEFAULT_MODEL,
        llava_model         : str  = ImageProcessor.DEFAULT_MODEL,
        llama_model         : str  = ChunkingAgent.MODEL,
        keyframe_interval   : int  = VideoProcessor.DEFAULT_KEYFRAME_INTERVAL,
        contextual_retrieval: bool = True,
        context_max_workers : int  = ContextualRetriever.DEFAULT_MAX_WORKERS,
        chunker_strategy    : str  = "markdown",   # "markdown" (default) or "llm"
    ) -> None:
        self._router  = DataRouter(
            whisper_model=whisper_model,
            llava_model=llava_model,
            keyframe_interval=keyframe_interval,
        )
        self._unifier = MarkdownUnifier()

        # Chunker selection. "markdown" is the new default — deterministic,
        # ~200× faster, immune to JSON parse failures, verbatim chunks.
        # "llm" preserves the original ChunkingAgent for users who want it.
        if chunker_strategy == "llm":
            self._chunker = ChunkingAgent(model=llama_model)
            logger.info("[MultimodalRAGPipeline] Using LLM chunker (legacy).")
        else:
            self._chunker = MarkdownChunker()
            logger.info("[MultimodalRAGPipeline] Using deterministic Markdown chunker.")

        self._url_proc = URLProcessor()

        self._use_contextual = contextual_retrieval
        self._ctx_retriever  = (
            ContextualRetriever(model=llama_model, max_workers=context_max_workers)
            if contextual_retrieval
            else None
        )

    def _chunk_and_enrich(
        self,
        unified: str,
        source_name: str,
        modality: str,
    ) -> list[ChunkDict]:
        """
        Shared tail of the pipeline: agentic chunking → metadata stamping →
        optional contextual enrichment. Used by both ingest() and ingest_url().
        """
        chunks = self._chunker.chunk(unified)

        # Attach source name + modality so downstream metadata filters work.
        for chunk in chunks:
            chunk["source"]   = source_name
            chunk["modality"] = modality

        if self._use_contextual and self._ctx_retriever is not None:
            chunks = self._ctx_retriever.enrich(chunks, full_document=unified)
        return chunks

    def ingest(self, file_path: str | Path) -> list[ChunkDict]:
        """
        Full pipeline: file → context-enriched chunks.

        Parameters
        ----------
        file_path : str | Path
            Any supported file (document, audio, image, video, code).

        Returns
        -------
        list[ChunkDict]
            Ready-to-index chunks.  Each dict contains:

            Always present
            ~~~~~~~~~~~~~~
            chunk_text          : str        original Markdown slice
            summary             : str        dense retrieval summary
            keywords            : list[str]  BM25 keywords

            Present when contextual_retrieval=True (default)
            ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
            context             : str        situating 1–2 sentences
            contextualized_text : str        context + chunk_text  ← EMBED THIS
            context_error       : str        only if contextualisation failed
        """
        path = Path(file_path)
        logger.info("═══ Pipeline start: '%s' ═══", path.name)

        # Step 1: Route → raw Markdown
        raw_markdown, modality = self._router.route(path)

        # Step 2: Unify → metadata header + Markdown
        unified = self._unifier.unify(
            markdown=raw_markdown,
            source=path,
            modality=modality,
        )

        # Steps 3–4: chunk, stamp metadata, contextual enrichment
        chunks = self._chunk_and_enrich(unified, source_name=path.name, modality=modality)

        logger.info(
            "═══ Pipeline complete: '%s' → %d chunks ═══",
            path.name,
            len(chunks),
        )
        return chunks

    def ingest_url(self, url: str) -> tuple[list[ChunkDict], str]:
        """
        Full pipeline for a web page: URL → context-enriched chunks.

        Returns
        -------
        (chunks, source_name)
            source_name is the page title (used as the 'source' in metadata).
        """
        logger.info("═══ Pipeline start (URL): '%s' ═══", url)

        # Step 1: Fetch + extract clean Markdown
        raw_markdown, title = self._url_proc.process(url)

        # Use a stable, human-readable source name. Prefer the page title;
        # fall back to the URL. Keep it short for clean display/citation.
        source_name = (title or url).strip()[:120]

        # Step 2: Unify → metadata header + Markdown (modality = "Web")
        unified = self._unifier.unify(
            markdown=raw_markdown,
            source=source_name,
            modality="Web",
        )

        # Steps 3–4: chunk, stamp metadata, contextual enrichment
        chunks = self._chunk_and_enrich(unified, source_name=source_name, modality="Web")

        logger.info(
            "═══ Pipeline complete (URL): '%s' → %d chunks ═══",
            source_name,
            len(chunks),
        )
        return chunks, source_name


