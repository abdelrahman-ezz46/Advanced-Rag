# ══════════════════════════════════════════════════════════════════════════════
#  BASE PROCESSOR
# ══════════════════════════════════════════════════════════════════════════════

class BaseProcessor(ABC):
    """
    Abstract base class for all modality processors.

    Every concrete processor MUST implement `process()`, which accepts a file
    path and returns a Markdown string.  Raising `ProcessingError` on failure
    allows the pipeline to handle errors gracefully without crashing.
    """

    class ProcessingError(RuntimeError):
        """Raised when a processor cannot handle the given file."""

    @abstractmethod
    def process(self, file_path: Path) -> str:
        """
        Process *file_path* and return its content as a Markdown string.

        Parameters
        ----------
        file_path : Path
            Absolute path to the source file.

        Returns
        -------
        str
            UTF-8 Markdown representing the file's content.

        Raises
        ------
        BaseProcessor.ProcessingError
            If processing fails for any recoverable reason.
        """


# ══════════════════════════════════════════════════════════════════════════════
#  1. DOCUMENT PROCESSOR  (PDF / DOCX → Markdown via Docling)
# ══════════════════════════════════════════════════════════════════════════════

class DocumentProcessor(BaseProcessor):
    """
    Converts PDF and Word documents into Markdown using **Docling**.

    Docling is a powerful document understanding library that:
      • Preserves document structure (headings, tables, lists, captions).
      • Handles multi-column layouts and embedded figures.
      • Runs entirely locally — no API calls.

    Install : pip install docling
    Docs    : https://github.com/DS4SD/docling
    """

    def process(self, file_path: Path) -> str:
        logger.info("[DocumentProcessor] Processing '%s'", file_path.name)

        try:
            # Lazy import so the script doesn't crash if docling is absent
            # and a different processor is used.
            from docling.document_converter import DocumentConverter  # type: ignore
        except ImportError as exc:
            raise self.ProcessingError(
                "Docling is not installed. Run: pip install docling"
            ) from exc

        try:
            converter = DocumentConverter()
            result = converter.convert(str(file_path))

            # Docling's export_to_markdown() returns a clean Markdown string.
            markdown_output = result.document.export_to_markdown()

            if not markdown_output.strip():
                raise self.ProcessingError(
                    f"Docling returned empty output for '{file_path.name}'. "
                    "The file may be image-only (scanned) or encrypted."
                )

            logger.info(
                "[DocumentProcessor] Success — %d characters", len(markdown_output)
            )
            return markdown_output

        except self.ProcessingError:
            raise  # re-raise our own errors unchanged
        except Exception as exc:
            raise self.ProcessingError(
                f"Docling failed to process '{file_path}': {exc}"
            ) from exc


# ══════════════════════════════════════════════════════════════════════════════
#  2. AUDIO PROCESSOR  (audio → transcript via local Whisper)
# ══════════════════════════════════════════════════════════════════════════════

class AudioProcessor(BaseProcessor):
    """
    Transcribes audio files to Markdown using OpenAI's **Whisper** model
    running entirely on-device (CPU or CUDA GPU).

    Whisper is loaded once and cached on the instance to avoid reloading
    for each file when processing many audio tracks (e.g. video keyframes).

    Install : pip install openai-whisper torch
    Models  : tiny | base | small | medium | large  (larger = more accurate)
    Docs    : https://github.com/openai/whisper
    """

    #: Whisper model size.  "base" balances speed and accuracy for most use cases.
    DEFAULT_MODEL = "base"

    def __init__(self, model_size: str = DEFAULT_MODEL) -> None:
        self._model_size = model_size
        self._model: Any = None  # lazy-loaded on first use

    def _load_model(self) -> Any:
        """Load (and cache) the Whisper model."""
        if self._model is None:
            try:
                import whisper  # type: ignore
            except ImportError as exc:
                raise self.ProcessingError(
                    "openai-whisper is not installed. Run: pip install openai-whisper torch"
                ) from exc

            logger.info(
                "[AudioProcessor] Loading Whisper '%s' model (first run downloads it)…",
                self._model_size,
            )
            self._model = whisper.load_model(self._model_size)
            logger.info("[AudioProcessor] Whisper model loaded.")

        return self._model

    def process(self, file_path: Path) -> str:
        logger.info("[AudioProcessor] Transcribing '%s'", file_path.name)

        model = self._load_model()

        try:
            # `transcribe()` returns a dict with 'text' and 'segments'.
            # We use segments to produce timestamped Markdown.
            result = model.transcribe(
                str(file_path),
                verbose=False,          # suppress per-segment console spam
                word_timestamps=False,  # segment-level timestamps are enough
            )
        except Exception as exc:
            raise self.ProcessingError(
                f"Whisper transcription failed for '{file_path}': {exc}"
            ) from exc

        segments: list[dict] = result.get("segments", [])
        full_text: str = result.get("text", "").strip()

        if not full_text:
            raise self.ProcessingError(
                f"Whisper returned empty transcription for '{file_path.name}'. "
                "Check that the file contains speech."
            )

        # ── Format as timestamped Markdown ──────────────────────────────────
        lines = ["## Transcript\n"]

        if segments:
            for seg in segments:
                start = self._fmt_ts(seg.get("start", 0.0))
                text  = seg.get("text", "").strip()
                if text:
                    lines.append(f"**[{start}]** {text}\n")
        else:
            # Fallback: no segment info, just emit the full text
            lines.append(full_text)

        markdown_output = "\n".join(lines)
        logger.info(
            "[AudioProcessor] Success — %d segments, %d chars",
            len(segments),
            len(markdown_output),
        )
        return markdown_output

    @staticmethod
    def _fmt_ts(seconds: float) -> str:
        """Convert a float seconds value to HH:MM:SS format."""
        secs = int(seconds)
        h, rem = divmod(secs, 3600)
        m, s   = divmod(rem, 60)
        return f"{h:02d}:{m:02d}:{s:02d}"


# ══════════════════════════════════════════════════════════════════════════════
#  3. IMAGE PROCESSOR  (image → Markdown description via Ollama + LLaVA)
# ══════════════════════════════════════════════════════════════════════════════

class ImageProcessor(BaseProcessor):
    """
    Sends images to a **LLaVA** vision model running locally via **Ollama**
    and prompts it to describe visual content, transcribe handwriting, or
    convert charts/tables into Markdown.

    LLaVA (Large Language-and-Vision Assistant) is a multimodal model that
    accepts both text prompts and image inputs.

    Install : pip install ollama Pillow
    Setup   : ollama pull llava   (downloads ~4 GB model)
    Docs    : https://ollama.com/library/llava
    """

    #: Ollama model tag for vision inference.
    DEFAULT_MODEL = "llava"

    #: System-level prompt that steers LLaVA toward structured Markdown output.
    VISION_PROMPT = (
        "You are a highly accurate document analysis assistant. "
        "Examine the provided image carefully and produce output in Markdown format.\n\n"
        "Follow these rules:\n"
        "1. **Charts / graphs**: Convert data into a Markdown table. "
        "   Include axis labels, legend entries, and approximate values.\n"
        "2. **Handwritten text**: Transcribe it verbatim inside a blockquote (`> `).\n"
        "3. **Diagrams / flowcharts**: Describe each node and edge in a numbered list.\n"
        "4. **Photographs / illustrations**: Write a detailed, factual description "
        "   (2–5 sentences).\n"
        "5. **Screenshots / UI**: Describe the interface layout and any visible text.\n\n"
        "Be precise. Do NOT add commentary or apologies. Output ONLY Markdown."
    )

    def __init__(self, model: str = DEFAULT_MODEL) -> None:
        self._model = model

    def process(self, file_path: Path) -> str:
        logger.info("[ImageProcessor] Analysing '%s' with %s", file_path.name, self._model)

        try:
            import ollama  # type: ignore
        except ImportError as exc:
            raise self.ProcessingError(
                "ollama is not installed. Run: pip install ollama"
            ) from exc

        # ── Validate the image is readable ──────────────────────────────────
        try:
            from PIL import Image  # type: ignore
            with Image.open(file_path) as img:
                img.verify()   # raises on corrupt files
        except Exception as exc:
            raise self.ProcessingError(
                f"Cannot open image '{file_path.name}': {exc}"
            ) from exc

        # ── Encode image as base64 for the Ollama API ────────────────────────
        image_b64 = base64.b64encode(file_path.read_bytes()).decode("utf-8")

        # ── Call LLaVA via Ollama ────────────────────────────────────────────
        try:
            response = ollama.chat(
                model=self._model,
                messages=[
                    {
                        "role": "user",
                        "content": self.VISION_PROMPT,
                        "images": [image_b64],
                    }
                ],
            )
        except Exception as exc:
            raise self.ProcessingError(
                f"Ollama/{self._model} call failed: {exc}. "
                "Ensure 'ollama serve' is running and 'ollama pull llava' was executed."
            ) from exc

        # ── Extract text from response ───────────────────────────────────────
        markdown_output: str = response["message"]["content"].strip()

        if not markdown_output:
            raise self.ProcessingError(
                f"LLaVA returned empty description for '{file_path.name}'."
            )

        logger.info("[ImageProcessor] Success — %d chars", len(markdown_output))
        return markdown_output

    def process_bytes(self, image_bytes: bytes, label: str = "frame") -> str:
        """
        Convenience method: accept raw image bytes instead of a file path.
        Used by VideoProcessor to handle in-memory keyframes.
        """
        with tempfile.NamedTemporaryFile(suffix=".jpg", delete=False) as tmp:
            tmp.write(image_bytes)
            tmp_path = Path(tmp.name)

        try:
            return self.process(tmp_path)
        finally:
            tmp_path.unlink(missing_ok=True)


# ══════════════════════════════════════════════════════════════════════════════
#  4. VIDEO PROCESSOR  (video → audio transcript + keyframe descriptions)
# ══════════════════════════════════════════════════════════════════════════════

class VideoProcessor(BaseProcessor):
    """
    Processes video files by:
      1. Using **FFmpeg** (via subprocess) to extract the audio track.
      2. Using **FFmpeg** to extract keyframes at a configurable interval.
      3. Passing audio → AudioProcessor for transcription.
      4. Passing each keyframe → ImageProcessor for visual description.
      5. Merging both outputs into a single, timestamped Markdown document.

    System dep : FFmpeg must be on PATH
    Install    : brew install ffmpeg  /  sudo apt-get install ffmpeg
    """

    #: How often (in seconds) to sample a keyframe.
    DEFAULT_KEYFRAME_INTERVAL = 30  # one frame every 30 s

    def __init__(
        self,
        keyframe_interval: int = DEFAULT_KEYFRAME_INTERVAL,
        whisper_model: str = AudioProcessor.DEFAULT_MODEL,
        llava_model:   str = ImageProcessor.DEFAULT_MODEL,
    ) -> None:
        self._keyframe_interval = keyframe_interval
        self._audio_proc = AudioProcessor(model_size=whisper_model)
        self._image_proc = ImageProcessor(model=llava_model)

    # ── Main entry point ─────────────────────────────────────────────────────

    def process(self, file_path: Path) -> str:
        logger.info("[VideoProcessor] Processing '%s'", file_path.name)
        self._check_ffmpeg()

        sections: list[str] = [f"# Video Analysis: {file_path.name}\n"]

        with tempfile.TemporaryDirectory(prefix="rag_video_") as tmpdir:
            tmp = Path(tmpdir)

            # ── Step 1: Extract audio and transcribe ─────────────────────────
            audio_md = self._extract_and_transcribe_audio(file_path, tmp)
            sections.append("## 🔊 Audio Transcript\n")
            sections.append(audio_md)
            sections.append("\n---\n")

            # ── Step 2: Extract keyframes and describe them ───────────────────
            frame_md = self._extract_and_describe_frames(file_path, tmp)
            sections.append("## 🎞️ Visual Keyframes\n")
            sections.append(frame_md)

        markdown_output = "\n".join(sections)
        logger.info("[VideoProcessor] Success — %d chars total", len(markdown_output))
        return markdown_output

    # ── Helpers ──────────────────────────────────────────────────────────────

    def _check_ffmpeg(self) -> None:
        """Verify FFmpeg is available on the system PATH."""
        try:
            subprocess.run(
                ["ffmpeg", "-version"],
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                check=True,
            )
        except (FileNotFoundError, subprocess.CalledProcessError) as exc:
            raise self.ProcessingError(
                "FFmpeg is not installed or not on PATH. "
                "Install it via your package manager or https://ffmpeg.org/download.html"
            ) from exc

    def _extract_and_transcribe_audio(self, video: Path, tmp: Path) -> str:
        """Demux audio track from video and transcribe with Whisper."""
        audio_path = tmp / "audio.wav"

        # FFmpeg flags:
        #   -vn         : disable video output
        #   -acodec pcm_s16le : standard WAV format, Whisper compatible
        #   -ar 16000   : 16 kHz sample rate (Whisper's native rate)
        #   -ac 1       : mono channel
        cmd = [
            "ffmpeg", "-y",           # overwrite without asking
            "-i", str(video),
            "-vn",
            "-acodec", "pcm_s16le",
            "-ar", "16000",
            "-ac", "1",
            "-pix_fmt", "yuv420p",
            str(audio_path),
        ]
        self._run_ffmpeg(cmd, label="audio extraction")

        try:
            return self._audio_proc.process(audio_path)
        except BaseProcessor.ProcessingError as exc:
            logger.warning("[VideoProcessor] Audio transcription failed: %s", exc)
            return "*Audio transcription unavailable.*\n"

    def _extract_and_describe_frames(self, video: Path, tmp: Path) -> str:
        """Extract keyframes and send each to LLaVA for description."""
        frames_dir = tmp / "frames"
        frames_dir.mkdir()

        # FFmpeg flags:
        #   -vf fps=1/N : one frame every N seconds
        #   %06d.jpg    : zero-padded sequential filenames
        cmd = [
            "ffmpeg", "-y",
            "-i", str(video),
            "-vf", f"fps=1/{self._keyframe_interval}",
            str(frames_dir / "%06d.jpg"),
        ]
        self._run_ffmpeg(cmd, label="keyframe extraction")

        frame_files = sorted(frames_dir.glob("*.jpg"))
        if not frame_files:
            logger.warning("[VideoProcessor] No keyframes extracted.")
            return "*No keyframes could be extracted.*\n"

        lines: list[str] = []
        for idx, frame_file in enumerate(frame_files):
            # Estimate the wall-clock timestamp of this frame
            timestamp_sec = idx * self._keyframe_interval
            ts_str = AudioProcessor._fmt_ts(float(timestamp_sec))

            lines.append(f"### Frame at {ts_str}\n")
            try:
                description = self._image_proc.process(frame_file)
                lines.append(description)
            except BaseProcessor.ProcessingError as exc:
                logger.warning("[VideoProcessor] Frame %s failed: %s", frame_file.name, exc)
                lines.append("*Frame description unavailable.*")
            lines.append("\n")

        return "\n".join(lines)

    @staticmethod
    def _run_ffmpeg(cmd: list[str], label: str) -> None:
        """Run an FFmpeg command, raising ProcessingError on failure."""
        logger.debug("[VideoProcessor] FFmpeg %s: %s", label, " ".join(cmd))
        try:
            result = subprocess.run(
                cmd,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.PIPE,
                check=True,
                timeout=600,  # 10-minute safety timeout
            )
        except subprocess.CalledProcessError as exc:
            stderr = exc.stderr.decode(errors="replace")
            raise BaseProcessor.ProcessingError(
                f"FFmpeg {label} failed:\n{stderr}"
            ) from exc
        except subprocess.TimeoutExpired as exc:
            raise BaseProcessor.ProcessingError(
                f"FFmpeg {label} timed out after 10 minutes."
            ) from exc


# ══════════════════════════════════════════════════════════════════════════════
#  5. CODE PROCESSOR  (source code → fenced Markdown code block)
# ══════════════════════════════════════════════════════════════════════════════

class CodeProcessor(BaseProcessor):
    """
    Wraps raw source-code files in a Markdown fenced code block with the
    appropriate language tag, preserving syntax for downstream LLM analysis.

    Supported extensions → language tags are defined in `EXTENSION_MAP`.
    Any unmapped extension falls back to a plain (untagged) code block.
    """

    EXTENSION_MAP: dict[str, str] = {
        ".py"   : "python",
        ".js"   : "javascript",
        ".ts"   : "typescript",
        ".java" : "java",
        ".c"    : "c",
        ".cpp"  : "cpp",
        ".cs"   : "csharp",
        ".go"   : "go",
        ".rs"   : "rust",
        ".rb"   : "ruby",
        ".sh"   : "bash",
        ".bash" : "bash",
        ".zsh"  : "bash",
        ".php"  : "php",
        ".swift": "swift",
        ".kt"   : "kotlin",
        ".r"    : "r",
        ".sql"  : "sql",
        ".html" : "html",
        ".css"  : "css",
        ".yaml" : "yaml",
        ".yml"  : "yaml",
        ".json" : "json",
        ".toml" : "toml",
        ".xml"  : "xml",
    }

    def process(self, file_path: Path) -> str:
        logger.info("[CodeProcessor] Wrapping '%s'", file_path.name)

        try:
            # Try UTF-8 first; fall back to latin-1 for legacy files
            try:
                code = file_path.read_text(encoding="utf-8")
            except UnicodeDecodeError:
                code = file_path.read_text(encoding="latin-1")
        except OSError as exc:
            raise self.ProcessingError(
                f"Cannot read source file '{file_path}': {exc}"
            ) from exc

        lang = self.EXTENSION_MAP.get(file_path.suffix.lower(), "")

        markdown_output = (
            f"## Source File: `{file_path.name}`\n\n"
            f"```{lang}\n"
            f"{code}\n"
            f"```\n"
        )

        logger.info("[CodeProcessor] Success — %d lines", code.count("\n") + 1)
        return markdown_output


# ══════════════════════════════════════════════════════════════════════════════
#  WEB URL PROCESSOR  (web page → clean Markdown)
# ══════════════════════════════════════════════════════════════════════════════

class URLProcessor:
    """
    Fetches a web page and extracts its main readable content as Markdown.

    Uses **trafilatura**, which strips nav bars, ads, footers, and boilerplate
    and returns just the article/body text — far cleaner than raw HTML scraping.
    Falls back to a bare <p>-tag extraction if trafilatura is unavailable.

    Note: this does NOT subclass BaseProcessor because its input is a URL
    string, not a file path. The pipeline calls it via a dedicated branch.

    Install : pip install trafilatura httpx
    """

    DEFAULT_TIMEOUT = 20  # seconds

    def __init__(self, timeout: int = DEFAULT_TIMEOUT) -> None:
        self._timeout = timeout

    def process(self, url: str) -> tuple[str, str]:
        """
        Fetch *url* and return (markdown_content, page_title).

        Raises
        ------
        BaseProcessor.ProcessingError
            On network failure or empty extraction.
        """
        logger.info("[URLProcessor] Fetching '%s'", url)

        try:
            import httpx  # type: ignore
        except ImportError as exc:
            raise BaseProcessor.ProcessingError(
                "httpx is not installed. Run: pip install httpx"
            ) from exc

        # ── Fetch the raw HTML ────────────────────────────────────────────
        try:
            headers = {"User-Agent": "Mozilla/5.0 (RAG-ingest; +local)"}
            resp = httpx.get(
                url, timeout=self._timeout, follow_redirects=True, headers=headers
            )
            resp.raise_for_status()
            html = resp.text
        except Exception as exc:
            raise BaseProcessor.ProcessingError(
                f"Failed to fetch '{url}': {exc}"
            ) from exc

        # ── Extract main content with trafilatura ─────────────────────────
        title = url
        markdown = ""
        try:
            import trafilatura  # type: ignore

            extracted = trafilatura.extract(
                html,
                output_format="markdown",
                include_links=False,
                include_images=False,
                with_metadata=False,
            )
            if extracted:
                markdown = extracted
            meta = trafilatura.extract_metadata(html)
            if meta and getattr(meta, "title", None):
                title = meta.title
        except ImportError:
            logger.warning(
                "[URLProcessor] trafilatura not installed — falling back to crude extraction."
            )
            markdown = self._fallback_extract(html)
        except Exception as exc:
            logger.warning("[URLProcessor] trafilatura failed (%s) — using fallback.", exc)
            markdown = self._fallback_extract(html)

        if not markdown.strip():
            raise BaseProcessor.ProcessingError(
                f"No readable content extracted from '{url}'."
            )

        # Prefix a heading so the source page is obvious in every chunk.
        markdown_output = f"# {title}\n\nSource URL: {url}\n\n{markdown}"
        logger.info("[URLProcessor] Success — %d chars from '%s'", len(markdown_output), title)
        return markdown_output, title

    @staticmethod
    def _fallback_extract(html: str) -> str:
        """Very crude <p>/<h*> text extraction when trafilatura is missing."""
        text = re.sub(r"(?is)<(script|style|nav|footer|header).*?</\1>", " ", html)
        text = re.sub(r"(?is)</p>|</h[1-6]>|<br\s*/?>", "\n", text)
        text = re.sub(r"(?s)<[^>]+>", " ", text)            # strip remaining tags
        text = re.sub(r"[ \t]+", " ", text)
        text = re.sub(r"\n\s*\n+", "\n\n", text)
        return text.strip()


# ══════════════════════════════════════════════════════════════════════════════
#  DATA ROUTER
# ══════════════════════════════════════════════════════════════════════════════

class DataRouter:
    """
    Central file-type dispatcher.

    Given a file path, `DataRouter.route()` infers the modality (by extension
    and MIME type), instantiates the correct `BaseProcessor` subclass, and
    returns the raw Markdown output.

    Routing priority: extension map → MIME type prefix → error.
    """

    # ── Extension → processor class mapping ─────────────────────────────────
    DOCUMENT_EXTS = {".pdf", ".docx", ".doc", ".odt", ".pptx", ".ppt", ".xlsx", ".xls"}
    AUDIO_EXTS    = {".mp3", ".wav", ".m4a", ".flac", ".ogg", ".aac", ".wma", ".opus"}
    IMAGE_EXTS    = {".jpg", ".jpeg", ".png", ".gif", ".bmp", ".tiff", ".tif", ".webp", ".svg"}
    VIDEO_EXTS    = {".mp4", ".avi", ".mov", ".mkv", ".webm", ".flv", ".wmv", ".m4v"}
    CODE_EXTS     = set(CodeProcessor.EXTENSION_MAP.keys())

    def __init__(
        self,
        whisper_model: str = AudioProcessor.DEFAULT_MODEL,
        llava_model:   str = ImageProcessor.DEFAULT_MODEL,
        keyframe_interval: int = VideoProcessor.DEFAULT_KEYFRAME_INTERVAL,
    ) -> None:
        # Pre-instantiate processors to share model caches across multiple files
        self._document_proc = DocumentProcessor()
        self._audio_proc    = AudioProcessor(model_size=whisper_model)
        self._image_proc    = ImageProcessor(model=llava_model)
        self._video_proc    = VideoProcessor(
            keyframe_interval=keyframe_interval,
            whisper_model=whisper_model,
            llava_model=llava_model,
        )
        self._code_proc     = CodeProcessor()

    def route(self, file_path: str | Path) -> tuple[str, str]:
        """
        Detect the file type and dispatch to the correct processor.

        Parameters
        ----------
        file_path : str | Path
            Path to the input file.

        Returns
        -------
        tuple[str, str]
            (markdown_content, modality_label)
            where modality_label is one of:
            "Document" | "Audio" | "Image" | "Video" | "Code"

        Raises
        ------
        FileNotFoundError
            If *file_path* does not exist.
        ValueError
            If the file type is not supported.
        BaseProcessor.ProcessingError
            Propagated from the underlying processor on processing failure.
        """
        path = Path(file_path).resolve()

        if not path.exists():
            raise FileNotFoundError(f"File not found: '{path}'")

        ext      = path.suffix.lower()
        modality = self._detect_modality(path, ext)

        logger.info("[DataRouter] '%s' → %s", path.name, modality)

        processor_map: dict[str, BaseProcessor] = {
            "Document" : self._document_proc,
            "Audio"    : self._audio_proc,
            "Image"    : self._image_proc,
            "Video"    : self._video_proc,
            "Code"     : self._code_proc,
        }

        processor = processor_map[modality]
        markdown  = processor.process(path)

        return markdown, modality

    def _detect_modality(self, path: Path, ext: str) -> str:
        """Determine modality from extension, falling back to MIME type."""
        if ext in self.DOCUMENT_EXTS: return "Document"
        if ext in self.AUDIO_EXTS:    return "Audio"
        if ext in self.IMAGE_EXTS:    return "Image"
        if ext in self.VIDEO_EXTS:    return "Video"
        if ext in self.CODE_EXTS:     return "Code"

        # MIME-type fallback
        mime, _ = mimetypes.guess_type(str(path))
        if mime:
            prefix = mime.split("/")[0]
            if prefix == "image":     return "Image"
            if prefix == "audio":     return "Audio"
            if prefix == "video":     return "Video"
            if prefix in ("text", "application"):
                # Heuristic: try to read as text; if it succeeds treat as Code
                try:
                    path.read_text(encoding="utf-8")
                    return "Code"
                except UnicodeDecodeError:
                    return "Document"

        raise ValueError(
            f"Unsupported file type: '{ext}' (MIME: {mime}). "
            "Add the extension to DataRouter's mapping or preprocess manually."
        )


# ══════════════════════════════════════════════════════════════════════════════
#  MARKDOWN UNIFIER
# ══════════════════════════════════════════════════════════════════════════════

class MarkdownUnifier:
    """
    Prepends a strict YAML-style metadata block to any raw Markdown.

    The metadata block makes every chunk self-describing and is critical for
    the retrieval stage: it lets the vector store filter by source, modality,
    or ingestion time without having to decode chunk content.

    Metadata fields
    ---------------
    Source    : original filename
    Modality  : "Document" | "Audio" | "Image" | "Video" | "Code"
    Timestamp : ISO-8601 UTC ingestion time
    """

    HEADER_TEMPLATE = """\
---
Source    : {source}
Modality  : {modality}
Timestamp : {timestamp}
---

"""

    def unify(
        self,
        markdown: str,
        source: str | Path,
        modality: str,
    ) -> str:
        """
        Prepend metadata to *markdown* and return the unified document.

        Parameters
        ----------
        markdown  : str
            Raw Markdown string produced by a processor.
        source    : str | Path
            Original filename or path (only the filename is stored).
        modality  : str
            Modality label from DataRouter.
        """
        timestamp = datetime.now(tz=timezone.utc).isoformat(timespec="seconds")
        source_name = Path(source).name

        header = self.HEADER_TEMPLATE.format(
            source=source_name,
            modality=modality,
            timestamp=timestamp,
        )

        unified = header + markdown.strip() + "\n"
        logger.info(
            "[MarkdownUnifier] Header applied — source='%s', modality='%s'",
            source_name,
            modality,
        )
        return unified


