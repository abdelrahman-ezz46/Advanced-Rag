def main() -> None:
    """
    Minimal command-line interface for the pipeline.

    Usage
    -----
      python multimodal_rag_pipeline.py <file_path> [output.json] [--no-context]

    Flags
    -----
      --no-context   Disable contextual retrieval (v1.0 behaviour).
                     Use this to benchmark the improvement CR provides.

    If no output path is given, results are printed to stdout.
    """
    import sys

    args = sys.argv[1:]

    # Parse --no-context flag
    use_context = True
    if "--no-context" in args:
        args.remove("--no-context")
        use_context = False

    if not args:
        print(
            "Usage: python multimodal_rag_pipeline.py <file_path> [output.json] [--no-context]\n\n"
            "Examples:\n"
            "  python multimodal_rag_pipeline.py report.pdf\n"
            "  python multimodal_rag_pipeline.py meeting.mp3 chunks.json\n"
            "  python multimodal_rag_pipeline.py diagram.png\n"
            "  python multimodal_rag_pipeline.py lecture.mp4 lecture_chunks.json\n"
            "  python multimodal_rag_pipeline.py main.py code_chunks.json\n"
            "  python multimodal_rag_pipeline.py report.pdf out.json --no-context\n"
        )
        sys.exit(1)

    file_path   = Path(args[0])
    output_path = Path(args[1]) if len(args) > 1 else None

    pipeline = MultimodalRAGPipeline(contextual_retrieval=use_context)

    try:
        chunks = pipeline.ingest(file_path)
    except FileNotFoundError as exc:
        logger.error("File not found: %s", exc)
        sys.exit(2)
    except BaseProcessor.ProcessingError as exc:
        logger.error("Processing failed: %s", exc)
        sys.exit(3)
    except Exception as exc:
        logger.exception("Unexpected error: %s", exc)
        sys.exit(4)

    json_output = json.dumps(chunks, indent=2, ensure_ascii=False)

    if output_path:
        output_path.write_text(json_output, encoding="utf-8")
        logger.info("Chunks written to '%s'", output_path)

        # Print a quick human-readable summary to stdout
        for i, chunk in enumerate(chunks, 1):
            ctx = chunk.get("context", "(contextual retrieval disabled)")
            kws = ", ".join(chunk.get("keywords", []))
            print(f"\n── Chunk {i} ──")
            print(f"  Context  : {ctx}")
            print(f"  Summary  : {chunk.get('summary', '')}")
            print(f"  Keywords : {kws}")
    else:
        print(json_output)


if __name__ == "__main__":
    main()
